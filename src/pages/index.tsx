import Head from 'next/head'
import { GetServerSideProps } from 'next'

import { Header } from '@/components/Header'
import { TopBar } from '@/components/TopBar'
import { HomeCategories } from '@/components/HomeCategories'
import { AdvantageSection } from '@/components/AdvantageSection'

import { Categories } from '@/models/Categories'

import { Box, Container, Heading, SimpleGrid, Text } from '@chakra-ui/react'
import { GroupedProducts, groupProductsByCategory } from '@/utils/groupProductsByCategory'
import { HomeProductsGrid } from '@/components/HomeProductsGrid'
import Image from 'next/image'

import bannerSeason from '/public/banner-new-season.jpeg'
import bannerStyle from '/public/banner-new-style.jpeg'

export type Product = {
  id: number,
  title: string,
  price: number,
  description: string,
  category: string,
  image: string,
  rating: {
    count: number,
    rate: number, 
  }
}


type Props ={
  products: Product[],
  categories: Categories[],
  groupProductsByCategory: GroupedProducts
}


export default function Home({products, categories, groupProductsByCategory}: Props) {
  return (
    <>
      <Head>
        <title>eCommerce Project</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

        <TopBar />
        <Box marginBottom="2rem">
          <Header />
        </Box>  
      <main>
        <Container size={{
          lg:'lg',
        }}>
          <HomeCategories categories={categories}/>

          <AdvantageSection/>

        </Container>  

        <Container maxW={{
          base: '100%',
          md: '1110px'
        }}>
          {Object.entries(groupProductsByCategory).map(([category, products])=>{
            return (
                <Box key={category} marginBottom='4rem'>
                  <Heading 
                    as='h2' 
                    size='md' 
                    textTransform='uppercase' m={{
                      base: '0 0 1rem 1rem',
                      md: '0 0 1.5rem'
                    }}
                  >
                    {category}
                  </Heading>
                  <HomeProductsGrid products={products}/>
                </Box>
              )
            })
          }
        </Container>

        <Container size={{
          lg:'lg',
        }}>
          <SimpleGrid 
            minChildWidth='255px' 
            spacing={{
              base: '1rem',
              md: '2rem'
            }}
          >
            <Image src={bannerSeason} alt=''/>
            <Image src={bannerStyle}  alt=''/>
          </SimpleGrid>
        </Container>
      </main>
    </>
  )
}

//Server side
export async function getServerSideProps(context: GetServerSideProps){
  const products = await fetch("https://fakestoreapi.com/products")
    .then(res => res.json())
  const categories = await fetch("https://fakestoreapi.com/products/categories")
    .then(res => res.json())

  const productsGroupedByCategory = groupProductsByCategory(products)


  return {
    props: {
      products,
      categories,
      productsGroupedByCategory
    }
  }
}
